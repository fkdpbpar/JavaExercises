
public class AreaAndPerimeterOfCircle {

    
    public static void main(String[] args) {
    float Radius=7.5f;
    double perimeter=(double)(2*Math.PI*Radius);
    double area=(double)(Math.PI*Radius*Radius);
    System.out.printf("The perimeter of circle with radius 7.5cm is %s cm%n",perimeter);
    System.out.printf("The area of circle with radius 7.5cm is %s cm*cm %n",area);
    }
    
}
